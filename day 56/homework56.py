def merge_strings(first, second):
    
    max_overlap = min(len(first), len(second))
    overlap = 0
    
    
    for i in range(max_overlap):
        if first[-(i+1):] == second[:i+1]:
            overlap = i + 1

    if overlap > 0:
        return first + second[overlap:]
    else:
        return first + second
    
